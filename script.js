// ========== الوضع الليلي ==========
const themeToggle = document.getElementById('themeToggle');
const html = document.documentElement;
let theme = localStorage.getItem('theme') || 'dark';
html.setAttribute('data-theme', theme);
themeToggle.textContent = theme === 'dark' ? '☀️' : '🌙';

themeToggle.addEventListener('click', () => {
  const newTheme = html.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
  html.setAttribute('data-theme', newTheme);
  localStorage.setItem('theme', newTheme);
  themeToggle.textContent = newTheme === 'dark' ? '☀️' : '🌙';
});

// ========== الترجمة ==========
let currentLang = localStorage.getItem('lang') || 'ar';
const langToggle = document.getElementById('langToggle');
const body = document.body;

function setLanguage(lang) {
  currentLang = lang;
  html.lang = lang;
  body.dir = lang === 'ar' ? 'rtl' : 'ltr';
  langToggle.textContent = lang === 'ar' ? 'EN' : 'AR';
  localStorage.setItem('lang', lang);

  // تحديث جميع العناصر التي تحمل الخاصية data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // تحديث placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // تحديث خيارات select (لأنها لا تُحدث تلقائياً)
  document.querySelectorAll('[data-i18n-options]').forEach(select => {
    // هذا يتطلب معالجة خاصة إذا أردنا تحديث الخيارات ديناميكياً
    // لكن بما أن لدينا خيارات ثابتة، سنقوم بتحديث النص المعروض فقط
    const selectedOption = select.options[select.selectedIndex];
    const key = selectedOption.getAttribute('data-i18n');
    if (key && translations[lang][key]) {
      selectedOption.textContent = translations[lang][key];
    }
  });

  // تحديث العملة
  document.querySelectorAll('.currency').forEach(el => {
    el.textContent = lang === 'ar' ? 'درهم' : 'MAD';
  });
}
setLanguage(currentLang);

langToggle.addEventListener('click', () => {
  setLanguage(currentLang === 'ar' ? 'en' : 'ar');
});

// ========== قائمة الهاتف ==========
const hamburger = document.querySelector('.hamburger');
const navMenu = document.querySelector('.nav-menu');

hamburger.addEventListener('click', () => {
  navMenu.classList.toggle('active');
});

document.querySelectorAll('.nav-menu a').forEach(link => {
  link.addEventListener('click', () => {
    navMenu.classList.remove('active');
  });
});

// ========== زر العودة إلى الأعلى ==========
const backToTop = document.getElementById('backToTop');

window.addEventListener('scroll', () => {
  if (window.scrollY > 300) {
    backToTop.classList.add('show');
  } else {
    backToTop.classList.remove('show');
  }
});

backToTop.addEventListener('click', () => {
  window.scrollTo({ top: 0, behavior: 'smooth' });
});

// ========== مؤشر القسم النشط ==========
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-menu a');

window.addEventListener('scroll', () => {
  let current = '';
  sections.forEach(section => {
    const sectionTop = section.offsetTop - 100;
    const sectionHeight = section.clientHeight;
    if (scrollY >= sectionTop && scrollY < sectionTop + sectionHeight) {
      current = section.getAttribute('id');
    }
  });

  navLinks.forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('href') === `#${current}`) {
      link.classList.add('active');
    }
  });
});

